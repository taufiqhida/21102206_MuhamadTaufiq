void main() {
  // Variable declaration with a type
  int myInt = 22; // Integer variable
  double myDouble = 20.8; // Double variable
  String myString = "Hello, Taufiq!"; // String variable
  bool myBool = false; // Boolean variable
  List<String> myList = ['Semarang', 'Banyumas', 'Jakarta']; // List of strings
  Map<String, int> myMap = {
    'apple': 1,
    'banana': 2,
  }; // Map with string keys and integer values

  // Print variables correctly
  print(myInt);
  print(myDouble);
  print(myString);
  print(myBool);
  print(myList);
  print(myMap);
}
